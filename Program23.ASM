INCLUDE LIBMACRO.INC
PROGRAM MASU
.CONST
VV DB 'VVEDIT CHUSLA ',0
DOT DB 'DODATNI ',0
KIL DB 'KILKIST DOTAT ',0
NUL DB 'KILKIST NULOVUX ',0
N DW 5
.DATA?
MAS DW  5 DUP(?)
START

MOV AX,03
INT 10H

WLINEZ VV
MOV DX,0
MOV BX,0
MOV CX,N
MOV SI,0
MOV DI,0
XX:
RWORD
MOV MAS[SI],AX
ADD SI,2
LOOP XX
MOV CX,N
WLINEZLN DOT
XX1:
MOV AX,MAS[DI]
CMP AX,0
JZ PLUS
JL KIN
WWORD
INC DX
JMP KIN
PLUS:
INC BX
KIN:
ADD DI,2
LOOP XX1
WLINEZLN KIL
WWORDLN DX
WLINEZLN NUL
WWORDLN BX
PAUSE
RETURN
END MASU