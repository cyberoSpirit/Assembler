INCLUDE LIBMACRO.INC
PROGRAM PACK
.DATA
MAS DB 4 DUP(?)
RES DB 0
START
MOV CX,4
MOV DI,0
CKL:
RWORD MAS[DI]
ADD DI,1
LOOP CKL
MOV CX,4
MOV SI,0
MOV BL,00000001B
CKL2:
MOV AL,MAS[SI]
SHL RES,1
SHR AL,2
AND AL,BL
OR RES,AL
SHL RES,1
MOV AL,MAS[SI]
SHR AL,4
AND AL,BL
OR RES,AL
ADD SI,1
LOOP CKL2
WWORD RES,,B
END PACK