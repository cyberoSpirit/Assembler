MAS DW 55 DUP(?)
X DB 5
VC DB 'VVEDIT CHUSLA',0
VR DB 'RESULTAT:',0

START
MOV AX,03
INT 10H

;WLINEZ VC
MOV CL,X
MOV DI,0
CKL1:
 RWORD AX
 MOV MAS[DI],AX
 ADD DI,2
 LOOP CKL1

MOV CL,X
MOV SI,0
MOV BX,1010110111110101B
MOV DX,1111111110101010B
WLINEZ VR
LINEFEED
CKL2:
 MOV AX,MAS[SI]
 TEST AX,DX
 JZ M1
 TEST AX,BX
 JZ M1
 OR MAS[SI],111111111111111B
 AND MAS[SI],111111000111111B

M1:WWORD MAS[SI],,B
   ADD SI,2
   ADD DI,2

LOOP CKL2

PAUSE
RETURN
END LOG5
