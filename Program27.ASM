PROGRAM TABUN
;____________________________________________________________________________
;OBROBKA TABLUTS
;ZA NOMEROM YNIVERY, VUVODUT JOGO DANI
;____________________________________________________________________________
.CONST
TAB DB 'LNYFRANKA',' 30000'
    DB 'POLITEXNI',' 20004'
    DB 'EVROPEISK',' 13008'
    DB 'MEDINSTUT',' 1410 '
    DB 'CHORNOVOL',' 2011 '
    DB 'VETAKADEM',' 4056 '
PIDK2 DB 'VVED NOMER YNIVERSUTETU',0
PIDK1 DB 'NOMER VID 1 DO 6',0
VYN DB 'YNIVER, KILKIST STUDENTIV: ',0
.DATA
NM DB 15 DUP(' ')
DCH DB ?
START

MOV AX,03
INT 10H

PV:
WLINEZLN PIDK1
WLINEZLN PIDK2
RCHAR
LINEFEED
MOV DCH,AL
AND AX,000FH
CMP AX,0
JLE PV
CMP AX,6
JG PV
 LEA SI,TAB
 DEC AX
 MOV BX,15
 MUL BX
 ADD SI,AX
 MOV CX,15
LEA DI,NM
REP MOVSB
MOV AL,DCH
WCHAR

WLINEZ VYN
WLINEZ NM

PAUSE
RETURN
END TABUN