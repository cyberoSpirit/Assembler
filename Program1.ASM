INCLUDE LIBMACRO.INC
PROGRAM V1
;VURAZ Y=(D*X-1)(P*X-1)(R*X-1)
.DATA
WX DB 'INPUT X=',0
WR DB 'INPUT R=',0
WD DB 'INPUT D=',0
WP DB 'INPUT P=',0
WA DB 'INPUT A=',0
WY DB 'Y=',0
WZ DB 'VURAZ Y=(D*X-1)(P*X-1)(R*X-1)-(A+1)(D+1)',0
X DW ?
R DW ?
D DW ?
P DW ?
A DW ?
Y DD ?
Z DD ?
RES DD ?

START
MOV AX,03
INT 10H

WLINEZ WZ
WLINEZ WX
RWORD X
WLINEZ WR
RWORD R
WLINEZ WD
RWORD D
WLINEZ WP
RWORD P
WLINEZ WA
RWORD A

MOV AX,R
IMUL X
DEC AX
MOV BX,AX
MOV AX,P
IMUL X
DEC AX
MOV CX,AX
MOV AX,D
IMUL X
DEC AX
IMUL CX
IMUL BX
MOV WORD PTR Y,AX
MOV WORD PTR Y+2,DX

MOV AX,A
INC AX
MOV BX,AX
MOV AX,D
INC AX
IMUL BX

MOV WORD PTR Z,AX
MOV WORD PTR Z+2,DX
MOV AX,WORD PTR Y
SBB AX,WORD PTR Z
MOV WORD PTR RES, AX
MOV DX,WORD PTR Y+2
SBB DX,WORD PTR Z+2
MOV WORD PTR RES+2, DX

WLINEZ WY
WDWORD RES
PAUSE
RETURN
END V1
