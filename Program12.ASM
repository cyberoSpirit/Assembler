INCLUDE LIBMACRO.INC
PROGRAM DZ2
;***-----------------------------------------------------***
;PRACTUCHNA #3
;PROGRAMA, VUKORUSTOVYUCHU MASUV ZNAXODUT KILKIST CHUSEL,
;JAKI LEZHAT Y PROMIZKY [-3;3]
;***-----------------------------------------------------*** 
.CONST
VN DB 'VVEDIT N = ',0
VP DB 'ZNACHENNIA NEVIRNE. ',0
VC DB 'VVEDIT CHUSLA = ',0
VK DB 'KILKIST CHUSEL = ',0

.DATA
MAS DW 100 DUP(?)
SUM DW ?
N DW ?

START
MOV AX,03
INT 10H
JMP M1
M2:WLINEZ VP

M1:WLINEZ VN
 RWORD N
 MOV AX,N
 CMP AX,0
 JLE M2

MOV CX,N
MOV DI,0

WLINEZ VC
CKL1:
 RWORD AX
 MOV MAS[DI],AX
 INC DI
 INC DI
 LOOP CKL1

;OBROBKA DANUX
MOV CX,N
MOV BX,0
MOV SI,0

CKL2:
 MOV AX,MAS[SI]
 CMP AX,-3
 JL M3
 CMP AX,3
 JG M3
 INC BX
M3:INC SI
   INC SI
   LOOP CKL2
WLINEZ VK
WWORD BX
PAUSE
RETURN
END DZ2