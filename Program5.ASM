INCLUDE LIBMACRO.INC
PROGRAM MASD
.DATA
VC DB 'VVEDIT CHUSLA=',0
VMAX DB 'MAX ELEMENTU RYADKIV:',0
VMIN DB 'MIN ELEMENTU RYADKIV:',0
VMAXS DB 'MAX ELEMENTU STOVPTSIV:',0
VMINS DB 'MIN ELEMENTU STOVPTSIV:',0
VN DB 'VVEDIT ROSMIRNIST N=',0
VM DB 'VVEDIT ROSMIRNIST M=',0

N DW ?
M DW ?
MAS DW 100 DUP(?)
MAX DW 100 DUP(?)
MIN DW 100 DUP(?)
MAXS DW 100 DUP(?)
MINS DW 100 DUP(?)
NM DW ?
M2 DW ?
N2 DW ?


START
MOV AX,03
INT 10H

WLINEZ VN
MOV BX,2
RWORD N
MOV AX,N
IMUL BX
MOV N2,AX

WLINEZ VN
RWORD M
MOV AX,M
IMUL BX
MOV M2,AX

MOV AX,N
IMUL M
MOV NM,AX

MOV CX,NM
MOV SI,0
CKL:
WLINEZ VC
RWORD
MOV MAS[SI],AX
ADD SI,2
LOOP CKL
;---------------
MOV CX,N
MOV SI,0
C1:PUSH CX
MOV CX,M
DEC CX
C2:WWORD MAS[SI]
ADD SI,2
LOOP C2
WWORDLN MAS[SI]
ADD SI,2
POP CX
LOOP C1
;---------------
MOV CX,N
MOV BX,0
MOV DI,0

CKLZ2:MOV AX,MAS[BX]
MOV MAX[DI],AX
MOV MIN[DI],AX
MOV SI,2
PUSH CX
MOV CX,M
DEC CX

CKLV2:
MOV AX,MAS[BX][SI]
CMP MAX[DI],AX
JGE M1
MOV MAX[DI],AX
M1:
;**************
CMP MIN[DI],AX
JLE MM2
MOV MIN[DI],AX
;**************
MM2: 
ADD SI,2
LOOP CKLV2
ADD BX,M2
POP CX
ADD DI,2
LOOP CKLZ2
;###############################################
MOV CX,M
MOV BX,0
MOV DI,0
CKLZ3:
 MOV AX,MAS[BX]
 MOV MAXS[DI],AX
 MOV MINS[DI],AX
 MOV SI,M2
 PUSH CX
 MOV CX,N
 DEC CX

 CKLV3:
  MOV AX,MAS[BX][SI]
  CMP MAXS[DI],AX
  JGE M3
  MOV MAXS[DI],AX
  M3:CMP MINS[DI],AX
  JLE M4
  MOV MINS[DI],AX
  M4:ADD SI,M2;
  LOOP CKLV3

 ADD BX,2
 POP CX
 ADD DI,2
 LOOP CKLZ3





WLINEZ VMAX
LINEFEED
MOV CX,N
MOV SI,0
CKLVV:
WWORD MAX[SI]
ADD SI,2
LOOP CKLVV
LINEFEED 
;----------
WLINEZ VMIN
LINEFEED
MOV CX,N
MOV SI,0
CKLVV2:
WWORD MIN[SI]
ADD SI,2
LOOP CKLVV2
;###############
LINEFEED
WLINEZ VMAXS
LINEFEED
MOV CX,M
MOV SI,0
CKLVV3:
WWORD MAXS[SI]
ADD SI,2
LOOP CKLVV3
;--------------
LINEFEED
WLINEZ VMINS
LINEFEED
MOV CX,M
MOV SI,0
CKLVV4:
WWORD MINS[SI]
ADD SI,2
LOOP CKLVV4

PAUSE
RETURN
END MASD