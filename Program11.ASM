INCLUDE LIBMACRO.INC
PROGRAM PRAC2_1
;Practuchna 2
;Variavt 19(1)
;Na plochuni 0xy poztashovani tru tochku a, b, c. Vuznachutu, jaka z tochok b, c znaxodutsia bluzche do a.
.CONST
K1 DB 'VVEDIT KOORDUNATU TOCHKU A=',0
K2 DB 'VVEDIT KOORDUNATU TOCHKU B=',0
K3 DB 'VVEDIT KOORDUNATU TOCHKU C=',0
BB DB 'TOCKA B BLUZCHE',0
CB DB 'TOCKA C BLUZCHE',0
OD DB 'ODUNAKOVO',0
.DATA
X1 DB ?
Y1 DB ?   ;A
X2 DB ?
Y2 DB ?   ;B
X3 DB ?
Y3 DB ?   ;C
START
MOV AX,03
INT 10H

WLINEZ K1
RWORD X1
RWORD Y1
WLINEZ K2
RWORD X2
RWORD Y2
WLINEZ K3
RWORD X3
RWORD Y3

MOV AL,X1
SUB AL,X2
IMUL AL
MOV BX,AX

MOV AL,Y1
SUB AL,Y2
IMUL AL

ADD AX,BX
MOV DX,AX

MOV AL,X1
SUB AL,X3
IMUL AL
MOV BX,AX

MOV AL,Y1
SUB AL,Y3
IMUL AL

ADD AX,BX

CMP DX,AX
JG CBLU

CMP DX,AX
JL BBL

WLINEZ OD
JMP FINISH
CBLU:WLINEZ CB

JMP FINISH
BBL: WLINEZ BB

FINISH:PAUSE
       RETURN
       END PRAC2_1