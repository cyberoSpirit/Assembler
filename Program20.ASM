INCLUDE LIBMACRO.INC
PROGRAM ZMIS
.DATA
VV DB 'VVED CHUSLO = ',0
VVREZ DB 'REZULTAT: ',0
MAS DW 100 DUP(?)
REZ DW 100 DUP(?)
N DW ?
START
MOV AX,03
INT 10H

WLINEZ VV
RWORD N
MOV CX,N
MOV SI,0
CKL:
RWORD MAS[SI]
ADD SI,2
LOOP CKL

MOV CX,9
MOV SI,0
MOV DI,0
CKL2:
MOV AX,MAS[SI]
SAL AX,3
ADD AX,MAS[SI]
MOV REZ[DI],AX
ADD DI,4
ADD SI,4
LOOP CKL2

MOV CX,9
MOV SI,2
MOV DI,2
CKL3:
MOV AX,MAS[SI]
SAR AX,2
MOV REZ[DI],AX
ADD DI,4
ADD SI,4
LOOP CKL3

WLINEZ VVREZ
LINEFEED
MOV CX,N
MOV DI,0
CKL4:
WWORD REZ[DI]
ADD DI,2
LOOP CKL4

PAUSE
RETURN
END ZMIS