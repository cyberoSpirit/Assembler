INCLUDE LIBMACRO.INC
PROGRAM PRAKT1
;OBCHUSLENNIA VURASY(2*X^2+Y+12)/(X^2+3).
;VUKORUSTANNIA STEKY
.CONST
PX DB 'VVEDIT X',0
PY DB 'VVEDIT Y',0
PZ DB 'RESULTAT=',0
.DATA 
X DW ?
Y DW ?
A DW ?
START

WLINEZ PX
RWORD X
WLINEZ PY
RWORD Y
MOV AX,X
MUL AX
ADD AX,3
PUSH AX
WWORD AX
PAUSE               ;V STEKY (3+X^2)

MOV AL,2
MUL X
MUL X
ADD AX,Y
ADD AX,12  ; (12+2*X^2+Y)
PUSH AX
WWORD AX
PAUSE

POP AX
POP BX

CWD 
DIV BX

WLINEZ PZ
WWORD AX
WWORD DX
PAUSE
RETURN
END PRAKT1