INCLUDE LIBMACRO.INC
PROGRAM YN
.DATA
A DB ?
B DB ?
X DW ?
C DB ?
VA DB 'VVEDIT A',0
VB DB 'VVEDIT B',0
VC DB 'VVEDIT C',0
VX DB 'VVEDIT X',0
VZ DB 'OBCH. VURASY Y=:',0
VZ1 DB '1)a/2-b/(3c),kolu x<-3;',0
VZ2 DB '2)a/2+b/(3a), kolu -3>=x>=0',0
VZ3 DB '3)(a+b)/2,kolu x>0.',0

START
MOV AX,03
INT 10H

WLINEZ VZ
LINEFEED
WLINEZ VZ1
LINEFEED
WLINEZ VZ2
LINEFEED
WLINEZ VZ3
LINEFEED

WLINEZ VA
RWORD A
WLINEZ VB
RWORD B

WLINEZ VX
RWORD X
MOV AX,X
CMP AX,0
JG BILL
CMP AX,-3
JL M3

MOV AL,6
IMUL A
MOV BH,AL
MOV AL,3
IMUL A
IMUL A
MOV AH,AL
MOV AL,2
IMUL B
ADD AL,AH
CBW
IDIV BH
WWORD AL
WWORD AH
JMP FIN

BILL:MOV BL,2
 MOV AL,A
 ADD AL,B
 CBW
 IDIV BL
 WWORD AL
 WWORD AH
 JMP FIN

M3:WLINEZ VC
 RWORD C

 MOV AL,6
 IMUL C
 MOV BH,AL
 MOV AL,3
 IMUL A
 IMUL C
 MOV BL,AL
 MOV AL,2
 IMUL B
 SUB BL,AL
 MOV AL,BL
 CBW
 IDIV BH
 WWORD AL
 WWORD AH
 JMP FIN



FIN:
PAUSE
RETURN
END YN