INCLUDE LIBMACRO.INC
PROGRAM OUTPACK
.DATA
A DB ?
MAS DB 4 DUP(0)
VZM DB 'VVED DANE  ',0
VMAS DB 'MAS = ',0
START
MOV AX,03
INT 10H

WLINEZ VZM
linefeed
RWORD A
MOV CX,4
MOV SI,0
MOV CL,0
MOV BL,0000001B
CKL1:
MOV AL,A
SAR AL,CL
AND AL,BL
SAL AL,3
OR MAS[SI],AL
INC CL
MOV AL,A
SAL AL,CL
AND AL,BL
SAL AL,5
OR MAS[SI],AL
INC CL
INC SI
LOOP CKL1

MOV CX,4
MOV SI,0
CKL2:
WWORD MAS[SI],,B
INC SI
LOOP CKL2


WLINEZ VMAS
PAUSE
RETURN
END OUTPACK