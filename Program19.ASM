INCLUDE LIBMACRO.INC
PROGRAM LOG7
.DATA
MAS DB 10 DUP(?)
VV DB 'VVEDIT DANI',0

START
MOV AX,03
INT 10H

WLINEZ VV
LINEFEED

MOV CX,3
MOV SI,0
CKL1:
RWORD AL
MOV MAS[SI],AL
ADD SI,1
LOOP CKL1

MOV DL,10000000B
MOV DH,00000001B
MOV CX,3
MOV SI,0
CKL2:

MOV AL,MAS[SI]
TEST AL,DL
JZ M1
TEST AL,DH
JNZ M1
XOR AL,00011000B
M1:
WWORD AL,,B
LINEFEED
ADD SI,1
LOOP CKL2

PAUSE
RETURN
END LOG7